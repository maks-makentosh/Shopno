'use strict'

var btn1 = document.getElementById('btn_1');
var item1 = document.getElementById('item_1');
var cl1 = document.getElementById('cl1');
btn1.onclick = function(){
    item1.style.background = '#70f5ea';
    btn1.style.display = 'none';
    cl1.style.display = 'block';
    cl1.style.color  = '#000';
}
cl1.onclick = function(){
    item1.style.background = 'linear-gradient(to right, rgba(0,0,0,0.65) 0%, rgba(0,0,0,0.65) 100%), url(img/service/service_1.png)';
    btn1.style.display = 'block';
    cl1.style.display = 'none';
}
